# Swap elements without using a temporary variable

# String values 

a = 'hello'
b = 'world'

print("Before swap:")
print("a:", a)
print("b:", b)

# Swapping the values
a, b = b, a

print("After swap:")
print("a:", a)
print("b:", b)  

# Numeric values
x = 5
y = 10

print("Before swap:")
print("x:", x)
print("y:", y)  

# Swapping the values
x, y = y, x

print("After swap:")
print("x:", x)
print("y:", y)